def validate_semantic_record(record: Dict[str, Any]) -> bool:
    missing = sorted(_REQUIRED_FIELDS - set(record))
    if missing:
        raise ValueError(f"Semantic record missing required fields: {', '.join(missing)}")
    if not isinstance(record["node_type"], str) or not record["node_type"]:
        raise ValueError("Semantic record node_type must be a non-empty string")
    for list_field in ("effects", "preconditions", "postconditions", "repair_hints"):
        if not isinstance(record[list_field], list):
            raise ValueError(f"Semantic record {record['node_type']} field {list_field} must be a list")
    for dict_field in ("inputs", "outputs", "field_behavior", "instance_behavior", "pairings"):
        if not isinstance(record[dict_field], dict):
            raise ValueError(f"Semantic record {record['node_type']} field {dict_field} must be an object")
    if not record["effects"]:
        raise ValueError(f"Semantic record {record['node_type']} must declare at least one effect")
    return True


def validate_semantic_catalog() -> Dict[str, Any]:
    for record in SEMANTIC_NODE_RECORDS.values():
        validate_semantic_record(record)
    return {
        "record_count": len(SEMANTIC_NODE_RECORDS),
        "node_types": list(SEMANTIC_NODE_RECORDS),
    }


def semantic_record_for_node(node_type: str) -> Dict[str, Any]:
    return copy.deepcopy(SEMANTIC_NODE_RECORDS.get(node_type, {}))


def nodes_for_effect(effect: str) -> List[str]:
    normalized = effect.strip().lower()
    return [
        node_type
        for node_type, record in SEMANTIC_NODE_RECORDS.items()
        if normalized in record.get("effects", [])
    ]


def legacy_semantic_for_node(node_type: str) -> Dict[str, Any]:
    return semantic_record_for_node(node_type)
