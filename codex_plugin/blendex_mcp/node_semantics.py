_REQUIRED_FIELDS = {
    "node_type",
    "role",
    "effects",
    "inputs",
    "outputs",
    "preconditions",
    "postconditions",
    "field_behavior",
    "instance_behavior",
    "pairings",
    "repair_hints",
}


def _socket(role: str, socket_type: str = "", value_kind: str = "") -> Dict[str, str]:
    return {"role": role, "socket_type": socket_type, "value_kind": value_kind}


def _record(
    node_type: str,
    *,
    role: str,
    common_use: str,
    effects: List[str],
    inputs: Dict[str, Dict[str, str]],
    outputs: Dict[str, Dict[str, str]],
    preconditions: List[str],
    postconditions: List[str],
    planning_hints: List[str],
    pairings: List[str],
    field_behavior: Dict[str, Any] = None,
    instance_behavior: Dict[str, Any] = None,
    repair_hints: List[str] = None,
) -> Dict[str, Any]:
    return {
        "node_type": node_type,
        "role": role,
        "common_use": common_use,
        "effects": list(effects),
        "inputs": copy.deepcopy(inputs),
        "outputs": copy.deepcopy(outputs),
        "typical_inputs": list(inputs),
        "typical_outputs": list(outputs),
        "preconditions": list(preconditions),
        "postconditions": list(postconditions),
        "field_behavior": copy.deepcopy(field_behavior or {}),
        "instance_behavior": copy.deepcopy(instance_behavior or {}),
        "planning_hints": list(planning_hints),
        "pairings": {"common": list(pairings), "avoid": []},
        "common_pairings": list(pairings),
        "repair_hints": list(repair_hints or []),
    }


